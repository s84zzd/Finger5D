/** 根据研究类型返回核心摘要的系统提示（模板说明） */
export function getCoreSummarySystemPrompt(
    studyType: "base" | "rct" | "meta" | "cohort" | "diagnostic"
): string {
    switch (studyType) {
        case "rct":
            return CORE_SUMMARY_RCT_PROMPT;
        case "meta":
            return CORE_SUMMARY_META_PROMPT;
        case "cohort":
            return CORE_SUMMARY_COHORT_PROMPT;
        case "diagnostic":
            return CORE_SUMMARY_DIAGNOSTIC_PROMPT;
        default:
            return CORE_SUMMARY_BASE_PROMPT;
    }
}
